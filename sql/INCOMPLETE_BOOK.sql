CREATE TABLE IF NOT EXISTS INCOMPLETE_BOOK(
    ISBN CHAR(13),
    TITLE VARCHAR(100),
    CREATORS VARCHAR,
    COPYRIGHT_DATE DATE,
    SUMMARY VARCHAR,
    SERIES_NAME_POSITION VARCHAR,
    GENRES VARCHAR,
    FORM VARCHAR,
    FORMAT VARCHAR,
    PAGES VARCHAR,
    TYPE VARCHAR,
    PUBLISHER VARCHAR,
    PUBLICATIONDATE DATE,
    AWARDS VARCHAR,
    READING_LEVEL VARCHAR,
    BANNED_BOOK BOOLEAN DEFAULT FALSE,
    TOPICS VARCHAR,
    SUBJECTS VARCHAR,
    TARGET_AUDIENCE VARCHAR,
    ALTERNATE_TITLES VARCHAR
)